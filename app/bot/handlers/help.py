from aiogram import F, Router
from aiogram.filters import Command
from aiogram.types import Message

from app.bot.keyboards import HELP_BUTTON, get_main_menu_keyboard


router = Router(name="help")


@router.message(Command("help"))
@router.message(F.text == HELP_BUTTON)
async def help_handler(message: Message) -> None:
    await message.answer(
        _build_help_text(),
        reply_markup=get_main_menu_keyboard(),
    )


def _build_help_text() -> str:
    return "\n".join(
        [
            "❓ Помощь",
            "",
            "Что умеет бот:",
            "• вести привычки с разной частотой",
            "• показывать, что запланировано на сегодня",
            "• напоминать в нужное время",
            "• считать серии и общий прогресс",
            "",
            "Какие частоты есть:",
            "• каждый день",
            "• через день",
            "• по дням недели",
            "",
            "Как начать:",
            "• нажми «➕ Добавить привычку»",
            "• введи название",
            "• выбери частоту",
            "• при желании включи напоминание",
            "",
            "Как работают напоминания:",
            "• открой карточку привычки",
            "• нажми «⏰ Напоминание»",
            "• сначала укажи своё местное время, потом время напоминания",
            "",
            "Что такое серия:",
            "• текущая серия — сколько раз подряд привычка выполнена по своему расписанию",
            "• лучшая серия — самый длинный такой отрезок",
            "",
            "Где посмотреть архив:",
            "• открой «📋 Мои привычки»",
            "• перейди в «🗂 Архив»",
            "",
            "Если что-то мешает или хочется улучшения, напиши в «💬 Обратная связь».",
        ]
    )
